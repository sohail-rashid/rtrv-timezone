export function applyColorTheme(themeId: string, mode: 'light' | 'dark') {
  const theme = COLOR_THEMES.find((t) => t.id === themeId) || COLOR_THEMES[0];
  const colors = mode === 'dark' ? theme.dark : theme.light;
  const root = document.documentElement;

  for (const [key, cssVar] of Object.entries(varMap)) {
    root.style.setProperty(cssVar, colors[key as keyof ThemeColors]);
  }
}

export function getDefaultColorTheme(): string {
  if (typeof window !== 'undefined') {
    return localStorage.getItem('world-clock-color-theme') || 'obsidian';
  }
  return 'obsidian';
}
